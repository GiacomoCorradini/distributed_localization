function [mux, muy, sigmax, sigmay] = PropError2(vals,errs)

x1sol     = vals(1);
y1sol     = vals(2);
theta1sol = vals(3);
cam1sol   = vals(4); 
x2sol     = vals(5);
y2sol     = vals(6);
theta2sol = vals(7);
cam2sol   = vals(8);

sx1        = errs(1);
sy1        = errs(2);
stheta1    = errs(3);
scam1      = errs(4); 
sx2        = errs(5);
sy2        = errs(6);
stheta2    = errs(7);
scam2      = errs(8);

mux = (tan(cam1sol+theta1sol)+(-1).*tan(cam2sol+theta2sol)).^(-1).*((-1) ...
  .*y1sol+y2sol+x1sol.*tan(cam1sol+theta1sol)+(-1).*x2sol.*tan( ...
  cam2sol+theta2sol));

muy = (tan(cam1sol+theta1sol)+(-1).*tan(cam2sol+theta2sol)).^(-1).*((-1) ...
  .*y1sol.*tan(cam2sol+theta2sol)+tan(cam1sol+theta1sol).*(y2sol+( ...
  x1sol+(-1).*x2sol).*tan(cam2sol+theta2sol)));

sigmax = ((tan(cam1sol+theta1sol)+(-1).*tan(cam2sol+theta2sol)).^(-4).*( ...
  scam2.^2.*sec(cam2sol+theta2sol).^4.*(y1sol+(-1).*y2sol+((-1).* ...
  x1sol+x2sol).*tan(cam1sol+theta1sol)).^2+stheta2.^2.*sec(cam2sol+ ...
  theta2sol).^4.*(y1sol+(-1).*y2sol+((-1).*x1sol+x2sol).*tan( ...
  cam1sol+theta1sol)).^2+sy1.^2.*(tan(cam1sol+theta1sol)+(-1).*tan( ...
  cam2sol+theta2sol)).^2+sy2.^2.*(tan(cam1sol+theta1sol)+(-1).*tan( ...
  cam2sol+theta2sol)).^2+sx1.^2.*tan(cam1sol+theta1sol).^2.*(tan( ...
  cam1sol+theta1sol)+(-1).*tan(cam2sol+theta2sol)).^2+sx2.^2.*(tan( ...
  cam1sol+theta1sol)+(-1).*tan(cam2sol+theta2sol)).^2.*tan(cam2sol+ ...
  theta2sol).^2+scam1.^2.*sec(cam1sol+theta1sol).^4.*(y1sol+(-1).* ...
  y2sol+((-1).*x1sol+x2sol).*tan(cam2sol+theta2sol)).^2+stheta1.^2.* ...
  sec(cam1sol+theta1sol).^4.*(y1sol+(-1).*y2sol+((-1).*x1sol+x2sol) ...
  .*tan(cam2sol+theta2sol)).^2)).^(1/2);

sigmay = ((tan(cam1sol+theta1sol)+(-1).*tan(cam2sol+theta2sol)).^(-4).*(( ...
  scam2.^2+stheta2.^2).*sec(cam2sol+theta2sol).^4.*tan(cam1sol+ ...
  theta1sol).^2.*((-1).*y1sol+y2sol+(x1sol+(-1).*x2sol).*tan( ...
  cam1sol+theta1sol)).^2+(-2).*sy1.^2.*tan(cam1sol+theta1sol).*tan( ...
  cam2sol+theta2sol).^3+sy1.^2.*tan(cam2sol+theta2sol).^4+(scam1.^2+ ...
  stheta1.^2).*sec(cam1sol+theta1sol).^4.*tan(cam2sol+theta2sol) ...
  .^2.*(y1sol+(-1).*y2sol+((-1).*x1sol+x2sol).*tan(cam2sol+ ...
  theta2sol)).^2+tan(cam1sol+theta1sol).^4.*(sy2.^2+(sx1.^2+sx2.^2) ...
  .*tan(cam2sol+theta2sol).^2)+(-2).*tan(cam1sol+theta1sol).^3.*tan( ...
  cam2sol+theta2sol).*(sy2.^2+(sx1.^2+sx2.^2).*tan(cam2sol+ ...
  theta2sol).^2)+tan(cam1sol+theta1sol).^2.*tan(cam2sol+theta2sol) ...
  .^2.*(sy1.^2+sy2.^2+(sx1.^2+sx2.^2).*tan(cam2sol+theta2sol).^2))) ...
  .^(1/2);

end